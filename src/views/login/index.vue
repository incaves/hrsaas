<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">Login Form</h3>
      </div>

      <el-form-item prop="mobile">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="mobile"
          v-model="loginForm.mobile"
          placeholder="mobile"
          name="mobile"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">Login</el-button>

    </el-form>
  </div>
</template>

<script>
import { validMobile } from '@/utils/validate'
import { mapActions } from 'vuex'

export default {
  name: 'Login',
  data() {
    // 校验手机号的自定义规则
    const validateMobile = (rule, value, callback) => {
      validMobile(value) ? callback() : callback(new Error('The phone number format is incorrect'))
    }
    return {
      loginForm: {
        mobile: '13800000002',
        password: '123456'
      },
      loginRules: {
        mobile: [
          { required: true, trigger: 'blur', message: 'Mobile phone number cannot be empty' },
          { validator: validateMobile, trigger: 'blur' }],
        password: [
          { required: true, trigger: 'blur', message: 'The password cannot be empty' },
          { min: 6, max: 16, message: 'The password contains 6 to 16 characters', trigger: 'blur' }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    ...mapActions(['user/login']), // 加锁的模块需要加路径
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(async isOk => {
        if (isOk) {
          // 校验成功
          try {
            this.loading = true
            await this['user/login'](this.loginForm)
            // 强行等待上行代码的成功 成功的话再执行下面的操作
            // await下面的代码 都是成功执行的代码
            this.$router.push('/')
          } catch (error) {
            // 响应拦截器已经处理 失败进入这里 success = false
            console.log(error)
          } finally {
            //  不论执行try 还是catch  都去关闭loading
            this.loading = false
          }
        }
      })
    }
  }
}
</script>

